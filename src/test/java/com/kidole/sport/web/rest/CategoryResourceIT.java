    @Test
    @Transactional
    public void createCategory() throws Exception {
        int databaseSizeBeforeCreate = categoryRepository.findAll().size();

        // Create the Category
        CategoryDTO categoryDTO = categoryMapper.toDto(category);
        restCategoryMockMvc.perform(post("/api/categories")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(categoryDTO)))
            .andExpect(status().isCreated());

        // Validate the Category in the database
        List<Category> categoryList = categoryRepository.findAll();
        assertThat(categoryList).hasSize(databaseSizeBeforeCreate + 1);
        Category testCategory = categoryList.get(categoryList.size() - 1);
        assertThat(testCategory.getCategoryName()).isEqualTo(DEFAULT_CATEGORY_NAME);
        assertThat(testCategory.getYearlimit()).isEqualTo(DEFAULT_YEARLIMIT);
        assertThat(testCategory.getTeamLimitNumb()).isEqualTo(DEFAULT_TEAM_LIMIT_NUMB);
        assertThat(testCategory.getParticipantLimitByteam()).isEqualTo(DEFAULT_PARTICIPANT_LIMIT_BYTEAM);
        assertThat(testCategory.getCategoryRule()).isEqualTo(DEFAULT_CATEGORY_RULE);

        // Validate the Category in Elasticsearch
        verify(mockCategorySearchRepository, times(1)).save(testCategory);
    }

