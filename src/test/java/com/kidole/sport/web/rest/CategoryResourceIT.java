    @Test
    @Transactional
    public void updateCategory() throws Exception {
        // Initialize the database
        categoryRepository.saveAndFlush(category);

        int databaseSizeBeforeUpdate = categoryRepository.findAll().size();

        // Update the category
        Category updatedCategory = categoryRepository.findById(category.getId()).get();
        // Disconnect from session so that the updates on updatedCategory are not directly saved in db
        em.detach(updatedCategory);
        updatedCategory
            .categoryName(UPDATED_CATEGORY_NAME)
            .yearlimit(UPDATED_YEARLIMIT)
            .teamLimitNumb(UPDATED_TEAM_LIMIT_NUMB)
            .participantLimitByteam(UPDATED_PARTICIPANT_LIMIT_BYTEAM)
            .categoryRule(UPDATED_CATEGORY_RULE);
        CategoryDTO categoryDTO = categoryMapper.toDto(updatedCategory);

        restCategoryMockMvc.perform(put("/api/categories")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(categoryDTO)))
            .andExpect(status().isOk());

        // Validate the Category in the database
        List<Category> categoryList = categoryRepository.findAll();
        assertThat(categoryList).hasSize(databaseSizeBeforeUpdate);
        Category testCategory = categoryList.get(categoryList.size() - 1);
        assertThat(testCategory.getCategoryName()).isEqualTo(UPDATED_CATEGORY_NAME);
        assertThat(testCategory.getYearlimit()).isEqualTo(UPDATED_YEARLIMIT);
        assertThat(testCategory.getTeamLimitNumb()).isEqualTo(UPDATED_TEAM_LIMIT_NUMB);
        assertThat(testCategory.getParticipantLimitByteam()).isEqualTo(UPDATED_PARTICIPANT_LIMIT_BYTEAM);
        assertThat(testCategory.getCategoryRule()).isEqualTo(UPDATED_CATEGORY_RULE);

        // Validate the Category in Elasticsearch
        verify(mockCategorySearchRepository, times(1)).save(testCategory);
    }

